export function getPastLife(name: string) {
    // Simple hash function for deterministic result
    let hash = 0;
    for (let i = 0; i < name.length; i++) {
        const char = name.charCodeAt(i);
        hash = ((hash << 5) - hash) + char;
        hash |= 0; // Convert to 32bit integer
    }

    const index = Math.abs(hash) % PAST_LIVES.length;
    const title = PAST_LIVES[index];

    // Random year from -30000 to 2024
    const yearStart = -30000;
    const yearEnd = 2024;
    const range = yearEnd - yearStart;
    const year = yearStart + (Math.abs(hash * 31) % range);

    return {
        title,
        year: year < 0 ? `기원전 ${Math.abs(year)}년` : `${year}년`
    };
}
